package Modelo;

/**
 *
 * @author User
 */
public class Producto {
    
    int codigo;
    String tipo;
    String nombre;
    float precio;

    public Producto() {
    }

    public Producto(String tipo, String nombre, float precio) {
        this.tipo = tipo;
        this.nombre = nombre;
        this.precio = precio;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public float getPrecio() {
        return precio;
    }

    public void setPrecio(float precio) {
        this.precio = precio;
    }
    
    
            
    
}
